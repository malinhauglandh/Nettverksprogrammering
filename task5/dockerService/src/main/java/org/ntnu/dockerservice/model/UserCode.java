package org.ntnu.dockerservice.model;

public class UserCode {

    private String code;
    private String output;

    public UserCode() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getOutput() {
        return output;
    }

    public void setOutput(String input) {
        this.output = input;
    }
}
